public class MaxNumber {

    public static int max (int a, int b){
        return (a > b) ? a : b;
    }
    
        public static void main (String args[]){
        int num1  = 25;
        int num2 = 50;
        int maxNumber = max(num1, num2);
        
            System.out.println(maxNumber);
           
    }
    
}
